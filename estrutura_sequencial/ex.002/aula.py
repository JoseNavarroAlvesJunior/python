#Faça um Programa que peça um número e então mostre a mensagem "O número informado foi [número].""


numero: float # Declaração da variável "numero" do tipo float.

numero = float(input("Informe um número: "))  # Converte a entrada para float
# (input) é a entrada que o usuario fornece

# Exibe a mensagem com o número informado pelo usuário
print(f'O número informado é {numero}.')  # (print) Mostra o número informado