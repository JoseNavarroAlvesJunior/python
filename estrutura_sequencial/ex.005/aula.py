# Faça um Programa que converta metros para centímetros.

# Declara a variável medida_metros como tipo float
medida_metros = float 

# Solicita ao usuário que informe a medida em metros e converte para float
medida_metros = float(input("Informe a medida em metros: "))

# Declara a variável medida_centimetros como tipo float
medida_centimetros = float

# Converte a medida de metros para centímetros multiplicando por 100
medida_centimetros = medida_metros * 100

# Exibe o resultado da conversão de metros para centímetros
print(f'{medida_metros} metros convertendo em centímetros é {medida_centimetros} centímetros.')
